package semi.dto;

public class DaySales {
	
	public String day;
	public int daySales;
	
	public DaySales() {}

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public int getDaySales() {
		return daySales;
	}

	public void setDaySales(int daySales) {
		this.daySales = daySales;
	}
	
	
	
}
