package semi.dto;

public class OrderDto {
	
	private int count;
	private String userId;
	private int orderNo;
	private int orderedProductNo;
	private int orderedAmount;
	private int orderedPrice;
	private int itemNo;
	private int UsedPoint;
	
	public OrderDto() {}
	
	public int getUsedPoint() {
		return UsedPoint;
	}

	public void setUsedPoint(int usedPoint) {
		UsedPoint = usedPoint;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public int getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(int orderNo) {
		this.orderNo = orderNo;
	}

	public int getOrderedProductNo() {
		return orderedProductNo;
	}

	public void setOrderedProductNo(int orderedProductNo) {
		this.orderedProductNo = orderedProductNo;
	}

	public int getOrderedAmount() {
		return orderedAmount;
	}

	public void setOrderedAmount(int orderedAmount) {
		this.orderedAmount = orderedAmount;
	}

	public int getOrderedPrice() {
		return orderedPrice;
	}

	public void setOrderedPrice(int orderedPrice) {
		this.orderedPrice = orderedPrice;
	}

	public int getItemNo() {
		return itemNo;
	}

	public void setItemNo(int itemNo) {
		this.itemNo = itemNo;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
